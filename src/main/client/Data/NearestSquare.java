	/**
	 * 
	 * @param width
	 * @param depth
	 */
	public NearestSquare(final int width, final int depth) {
		
		numSquares = (width-2)*(depth-1);
		xValues = new int[numSquares];
		yValues = new int[numSquares];
		LoadValues();
	}
	
	/**
	 * Returns the number of squares this class accounts for,
	 * use this to prevent requesting a square index that is out of bounds.
	 * @return The number of squares this class accounts for.
	 */
